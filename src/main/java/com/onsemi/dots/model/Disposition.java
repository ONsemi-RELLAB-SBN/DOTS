package com.onsemi.dots.model;

public class Disposition {

	private String id;
	private String requestId;
	private String remarks;
	private String disposition;
	private String dispoBy;
	private String dispoDate;
	private String flag;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRequestId() {
		return requestId;
	}

	public void setRequestId(String requestId) {
		this.requestId = requestId;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getDisposition() {
		return disposition;
	}

	public void setDisposition(String disposition) {
		this.disposition = disposition;
	}

	public String getDispoBy() {
		return dispoBy;
	}

	public void setDispoBy(String dispoBy) {
		this.dispoBy = dispoBy;
	}

	public String getDispoDate() {
		return dispoDate;
	}

	public void setDispoDate(String dispoDate) {
		this.dispoDate = dispoDate;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

}