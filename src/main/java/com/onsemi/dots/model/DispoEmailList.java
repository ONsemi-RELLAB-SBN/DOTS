package com.onsemi.dots.model;

public class DispoEmailList {

	private String id;
	private String distList;
	private String oncid;
	private String name;
	private String email;
	private String remarks;
	private String flag;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDistList() {
		return distList;
	}

	public void setDistList(String distList) {
		this.distList = distList;
	}

	public String getOncid() {
		return oncid;
	}

	public void setOncid(String oncid) {
		this.oncid = oncid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

}